<div class ="boxx">
  <p>Rice is a staple in various cuisines worldwide and holds a significant place in Ghanaian dishes. Different types of rice offer diverse textures and flavors, contributing to the richness of local cuisine.</p>

  <h3>1. Jasmine Rice</h3>
  <p>Jasmine rice, known for its fragrant aroma and slightly sticky texture when cooked, is a popular choice in Ghanaian cooking. It complements many dishes and is often used in flavorful rice-based recipes.</p>
  
  <h3>2. Basmati Rice</h3>
  <p>Basmati rice, with its long grains and distinct nutty flavor, is commonly used in Ghanaian households. It pairs well with various spicy dishes and is a key ingredient in many traditional meals.</p>
  
  <h3>3. Brown Rice</h3>
  <p>Brown rice, appreciated for its nutty taste and chewy texture, has gained popularity as a healthier alternative to white rice. It contains more nutrients and fiber, making it a nutritious choice for Ghanaian cuisine.</p>
  
  <h3>4. Local Varieties</h3>
  <p>Ghana also cultivates and consumes local varieties of rice, each with its own unique characteristics. These varieties, often found in local markets, offer diverse tastes and textures, contributing to the culinary richness of Ghana.</p>
  
</div>

